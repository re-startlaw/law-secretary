def next_dst(base: Path) -> Path:
    """_2, _3 ... と空き番号を探す。"""
    stem = base.stem  # 260507_通知書（内容証明郵便案）_2
    parent = base.parent
    suffix = base.suffix
    # base 自体が "_2"。存在すれば _3, _4 と探す
    if not base.exists():
        return base
    n = 3
    while True:
        cand = parent / f"260507_通知書（内容証明郵便案）_{n}{suffix}"
        if not cand.exists():
            return cand
        n += 1



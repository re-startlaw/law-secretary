def add_para(doc, text, alignment=WD_ALIGN_PARAGRAPH.LEFT, *, bold=False, size=None):
    p = doc.add_paragraph()
    p.alignment = alignment
    run = p.add_run(text)
    if bold:
        run.bold = True
    if size is not None:
        run.font.size = Pt(size)
    return p



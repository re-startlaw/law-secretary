def replace_text(p, text: str) -> None:
    for r in list(p.runs):
        r._element.getparent().remove(r._element)
    if text:
        p.add_run(text)


def main() -> None:
    assert SRC.exists(), SRC
    shutil.copy2(SRC, DEST)

    doc = Document(DEST)
    paras = list(doc.paragraphs)

    assert len(paras) == len(PARAS_ZH), (
        f"段落数不一致: src={len(paras)} zh={len(PARAS_ZH)}"
    )

    for p, text in zip(paras, PARAS_ZH):
        replace_text(p, text)

    doc.save(DEST)
    print(f"保存: {DEST}")


if __name__ == "__main__":
    main()

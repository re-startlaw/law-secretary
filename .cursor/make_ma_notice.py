def replace_paragraph_text(p, text: str) -> None:
    """段落のテキストを差し替える。最初のrunのフォントを保持して全てのrunを統合する。"""
    # 既存runを全て削除
    for r in list(p.runs):
        r._element.getparent().remove(r._element)
    # 新しいrunを追加
    if text:
        p.add_run(text)


def main() -> None:
    assert TEMPLATE.exists(), TEMPLATE
    assert DEST_DIR.exists(), DEST_DIR
    if DEST.exists():
        print(f"既存: {DEST} → 上書き")
    shutil.copy2(TEMPLATE, DEST)

    doc = Document(DEST)

    template_paras = list(doc.paragraphs)
    n_new = len(PARAS)
    n_tpl = len(template_paras)

    # 既存段落を上書き
    for i in range(min(n_tpl, n_new)):
        text, align = PARAS[i]
        p = template_paras[i]
        replace_paragraph_text(p, text)
        p.alignment = align

    # 新規が多い場合は末尾に追加（最後のテンプレ段落をコピーして書き換え）
    if n_new > n_tpl:
        last_tpl = template_paras[-1]
        body = last_tpl._element.getparent()
        for i in range(n_tpl, n_new):
            text, align = PARAS[i]
            new_p_xml = deepcopy(last_tpl._element)
            body.append(new_p_xml)
            new_p = doc.paragraphs[-1]
            replace_paragraph_text(new_p, text)
            new_p.alignment = align

    # テンプレが多い場合は末尾の余分な段落を削除
    if n_new < n_tpl:
        for i in range(n_tpl - 1, n_new - 1, -1):
            p = template_paras[i]
            p._element.getparent().remove(p._element)

    doc.save(DEST)
    print(f"保存: {DEST}")

    # 禁止文字スキャン（内容証明用）
    forbidden = (
        "⑴⑵⑶⑷⑸⑹⑺⑻⑼⑽①②③④⑤⑥⑦⑧⑨⑩"
        "ⅠⅡⅢⅣⅤⅥⅦⅧⅨⅩⅰⅱⅲⅳⅴⅵⅶⅷⅸⅹ"
        "㎡㎥㎝㎜㎞㎏㎎㍑℃℡№㈱㈲㈳㈴㍻㍼㍽㍾〝〟〃"
    )
    full = "\n".join(t for t, _ in PARAS)
    hit = [ch for ch in forbidden if ch in full]
    print(f"禁止文字検出: {hit if hit else 'なし'}")


if __name__ == "__main__":
    main()

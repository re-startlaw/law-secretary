def add(text="", *, align=None, bold=False, size=BODY_PT):
    p = doc.add_paragraph()
    if align is not None:
        p.alignment = align
    if text:
        r = p.add_run(text)
        r.font.name = FONT
        r_rpr = r._element.get_or_add_rPr()
        r_rfonts = r_rpr.find(qn("w:rFonts"))
        if r_rfonts is None:
            from docx.oxml import OxmlElement

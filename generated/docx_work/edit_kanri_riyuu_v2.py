def next_version_path(path: Path) -> Path:
    stem = path.stem
    suffix = path.suffix
    for i in range(2, 100):
        candidate = path.with_name(f"{stem}_{i}{suffix}")
        if not candidate.exists():
            return candidate
    raise RuntimeError("No available version suffix")


